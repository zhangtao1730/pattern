package com.study.singleton;
/**
 * 测试饿汉式单例模式
 * @author zhangtao
 *
 */
public class SingletonDemo1 {

	//类初始化时立即加载(没有延时加载的优势)
	//由于加载类时天然是线程安全的
	private static SingletonDemo1 instance=new SingletonDemo1();
	
	private SingletonDemo1(){
		
	}
	//方法没有同步，调用效率高
	public static SingletonDemo1 getInstance(){
		return instance;
	}
}
